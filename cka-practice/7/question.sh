Install Argo CD in the cluster by performing the following tasks:
🔹 Add the official Argo CD Helm repository https://argoproj.github.io/argo-helm with the name argo.
🔹 The Argo CD CRDS have already been pre-installed in the cluster.
🔹 Generate a helm template of the Argo CD Helm chart version 7.7.3 for the argocd namespace and save it to ~/argo-helmtemplate.yaml. Configure the chart to not install CRDs .
🔹 Install Argo CD using Helm with release name argocd using the same version and configuration as used in the template 7.7.3.
🔹 Install it in the argocd namespace and configure it to not install CRDs
🔹 Note: You do not need to configure access to the Argo CD server UI.

